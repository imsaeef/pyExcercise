turtle.forward(100)

# square shape
turtle.shape('square')
turtle.right(60)
turtle.forward(100)

# arrow shape
turtle.shape('arrow')
turtle.right(60)
turtle.forward(100)

# turtle shape
turtle.shape('turtle')
turtle.right(60)
turtle.forward(100)

turtle.exitonclick()